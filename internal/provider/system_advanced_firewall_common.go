func (m *SystemAdvancedFirewallModel) Set(_ context.Context, a pfsense.AdvancedFirewall) diag.Diagnostics {
	var diags diag.Diagnostics

	// Packet Processing
	m.ScrubNoDF = types.BoolValue(a.ScrubNoDF)
	m.ScrubRNID = types.BoolValue(a.ScrubRNID)
	m.Optimization = types.StringValue(a.Optimization)
	m.DisableScrub = types.BoolValue(a.DisableScrub)
	m.AdaptiveStart = types.Int64Value(int64(a.AdaptiveStart))
	m.AdaptiveEnd = types.Int64Value(int64(a.AdaptiveEnd))
	m.MaximumStates = types.Int64Value(int64(a.MaximumStates))
	m.MaximumTableEntries = types.Int64Value(int64(a.MaximumTableEntries))
	m.MaximumFragments = types.Int64Value(int64(a.MaximumFragments))

	// VPN Packet Processing
	m.VPNScrubNoDF = types.BoolValue(a.VPNScrubNoDF)
	m.VPNFragmentReassemble = types.BoolValue(a.VPNFragmentReassemble)
	m.MaxMSSEnable = types.BoolValue(a.MaxMSSEnable)
	m.MaxMSS = types.Int64Value(int64(a.MaxMSS))

	// Advanced Options
	m.DisableFirewall = types.BoolValue(a.DisableFirewall)
	m.BypassStaticRoutes = types.BoolValue(a.BypassStaticRoutes)
	m.DisableVPNRules = types.BoolValue(a.DisableVPNRules)
	m.DisableReplyTo = types.BoolValue(a.DisableReplyTo)
	m.DisableNegate = types.BoolValue(a.DisableNegate)
	m.NoAPIPA = types.BoolValue(a.NoAPIPA)
	m.AliasesResolveInterval = types.Int64Value(int64(a.AliasesResolveInterval))
	m.CheckAliasesURLCert = types.BoolValue(a.CheckAliasesURLCert)

	// Bogon Networks
	m.BogonsInterval = types.StringValue(a.BogonsInterval)

	// NAT
	m.NATReflection = types.StringValue(a.NATReflection)
	m.ReflectionTimeout = types.Int64Value(int64(a.ReflectionTimeout))
	m.EnableBINATReflection = types.BoolValue(a.EnableBINATReflection)
	m.EnableNATReflectionHelper = types.BoolValue(a.EnableNATReflectionHelper)

	if a.TFTPInterface != "" {
		m.TFTPInterface = types.StringValue(a.TFTPInterface)
	} else {
		m.TFTPInterface = types.StringNull()
	}

	// State Timeouts
	m.TCPFirstTimeout = types.Int64Value(int64(a.TCPFirstTimeout))
	m.TCPOpeningTimeout = types.Int64Value(int64(a.TCPOpeningTimeout))
	m.TCPEstablishedTimeout = types.Int64Value(int64(a.TCPEstablishedTimeout))
	m.TCPClosingTimeout = types.Int64Value(int64(a.TCPClosingTimeout))
	m.TCPFinWaitTimeout = types.Int64Value(int64(a.TCPFinWaitTimeout))
	m.TCPClosedTimeout = types.Int64Value(int64(a.TCPClosedTimeout))
	m.TCPTSDiffTimeout = types.Int64Value(int64(a.TCPTSDiffTimeout))
	m.UDPFirstTimeout = types.Int64Value(int64(a.UDPFirstTimeout))
	m.UDPSingleTimeout = types.Int64Value(int64(a.UDPSingleTimeout))
	m.UDPMultipleTimeout = types.Int64Value(int64(a.UDPMultipleTimeout))
	m.ICMPFirstTimeout = types.Int64Value(int64(a.ICMPFirstTimeout))
	m.ICMPErrorTimeout = types.Int64Value(int64(a.ICMPErrorTimeout))
	m.OtherFirstTimeout = types.Int64Value(int64(a.OtherFirstTimeout))
	m.OtherSingleTimeout = types.Int64Value(int64(a.OtherSingleTimeout))
	m.OtherMultipleTimeout = types.Int64Value(int64(a.OtherMultipleTimeout))

	return diags
}

func (m SystemAdvancedFirewallModel) Value(_ context.Context, a *pfsense.AdvancedFirewall) diag.Diagnostics {
	var diags diag.Diagnostics

	// Packet Processing
	a.ScrubNoDF = m.ScrubNoDF.ValueBool()
	a.ScrubRNID = m.ScrubRNID.ValueBool()

	addPathError(
		&diags,
		path.Root("firewall_optimization"),
		"Firewall optimization cannot be parsed",
		a.SetOptimization(m.Optimization.ValueString()),
	)

	a.DisableScrub = m.DisableScrub.ValueBool()
	a.AdaptiveStart = int(m.AdaptiveStart.ValueInt64())
	a.AdaptiveEnd = int(m.AdaptiveEnd.ValueInt64())
	a.MaximumStates = int(m.MaximumStates.ValueInt64())
	a.MaximumTableEntries = int(m.MaximumTableEntries.ValueInt64())
	a.MaximumFragments = int(m.MaximumFragments.ValueInt64())

	// VPN Packet Processing
	a.VPNScrubNoDF = m.VPNScrubNoDF.ValueBool()
	a.VPNFragmentReassemble = m.VPNFragmentReassemble.ValueBool()
	a.MaxMSSEnable = m.MaxMSSEnable.ValueBool()

	if a.MaxMSSEnable && !m.MaxMSS.IsNull() && m.MaxMSS.ValueInt64() > 0 {
		addPathError(
			&diags,
			path.Root("max_mss"),
			"Max MSS cannot be parsed",
			a.SetMaxMSS(int(m.MaxMSS.ValueInt64())),
		)
	} else {
		a.MaxMSS = int(m.MaxMSS.ValueInt64())
	}

	// Advanced Options
	a.DisableFirewall = m.DisableFirewall.ValueBool()
	a.BypassStaticRoutes = m.BypassStaticRoutes.ValueBool()
	a.DisableVPNRules = m.DisableVPNRules.ValueBool()
	a.DisableReplyTo = m.DisableReplyTo.ValueBool()
	a.DisableNegate = m.DisableNegate.ValueBool()
	a.NoAPIPA = m.NoAPIPA.ValueBool()
	a.AliasesResolveInterval = int(m.AliasesResolveInterval.ValueInt64())
	a.CheckAliasesURLCert = m.CheckAliasesURLCert.ValueBool()

	// Bogon Networks
	addPathError(
		&diags,
		path.Root("bogons_update_interval"),
		"Bogons interval cannot be parsed",
		a.SetBogonsInterval(m.BogonsInterval.ValueString()),
	)

	// NAT
	addPathError(
		&diags,
		path.Root("nat_reflection_mode"),
		"NAT reflection mode cannot be parsed",
		a.SetNATReflection(m.NATReflection.ValueString()),
	)

	a.ReflectionTimeout = int(m.ReflectionTimeout.ValueInt64())
	a.EnableBINATReflection = m.EnableBINATReflection.ValueBool()
	a.EnableNATReflectionHelper = m.EnableNATReflectionHelper.ValueBool()

	if !m.TFTPInterface.IsNull() {
		a.TFTPInterface = m.TFTPInterface.ValueString()
	}

	// State Timeouts
	a.TCPFirstTimeout = int(m.TCPFirstTimeout.ValueInt64())
	a.TCPOpeningTimeout = int(m.TCPOpeningTimeout.ValueInt64())
	a.TCPEstablishedTimeout = int(m.TCPEstablishedTimeout.ValueInt64())
	a.TCPClosingTimeout = int(m.TCPClosingTimeout.ValueInt64())
	a.TCPFinWaitTimeout = int(m.TCPFinWaitTimeout.ValueInt64())
	a.TCPClosedTimeout = int(m.TCPClosedTimeout.ValueInt64())
	a.TCPTSDiffTimeout = int(m.TCPTSDiffTimeout.ValueInt64())
	a.UDPFirstTimeout = int(m.UDPFirstTimeout.ValueInt64())
	a.UDPSingleTimeout = int(m.UDPSingleTimeout.ValueInt64())
	a.UDPMultipleTimeout = int(m.UDPMultipleTimeout.ValueInt64())
	a.ICMPFirstTimeout = int(m.ICMPFirstTimeout.ValueInt64())
	a.ICMPErrorTimeout = int(m.ICMPErrorTimeout.ValueInt64())
	a.OtherFirstTimeout = int(m.OtherFirstTimeout.ValueInt64())
	a.OtherSingleTimeout = int(m.OtherSingleTimeout.ValueInt64())
	a.OtherMultipleTimeout = int(m.OtherMultipleTimeout.ValueInt64())

	return diags
}
