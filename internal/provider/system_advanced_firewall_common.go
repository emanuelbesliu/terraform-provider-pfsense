// SystemAdvancedFirewallModel represents the Terraform model for system advanced firewall & NAT settings.
type SystemAdvancedFirewallModel struct {
	// Packet Processing
	ScrubNoDF           types.Bool   `tfsdk:"scrub_no_df"`
	ScrubRNID           types.Bool   `tfsdk:"scrub_random_id"`
	Optimization        types.String `tfsdk:"firewall_optimization"`
	DisableScrub        types.Bool   `tfsdk:"disable_scrub"`
	AdaptiveStart       types.Int64  `tfsdk:"adaptive_start"`
	AdaptiveEnd         types.Int64  `tfsdk:"adaptive_end"`
	MaximumStates       types.Int64  `tfsdk:"maximum_states"`
	MaximumTableEntries types.Int64  `tfsdk:"maximum_table_entries"`
	MaximumFragments    types.Int64  `tfsdk:"maximum_fragment_entries"`

	// VPN Packet Processing
	VPNScrubNoDF          types.Bool  `tfsdk:"vpn_scrub_no_df"`
	VPNFragmentReassemble types.Bool  `tfsdk:"vpn_fragment_reassemble"`
	MaxMSSEnable          types.Bool  `tfsdk:"max_mss_enable"`
	MaxMSS                types.Int64 `tfsdk:"max_mss"`

	// Advanced Options
	DisableFirewall        types.Bool  `tfsdk:"disable_firewall"`
	BypassStaticRoutes     types.Bool  `tfsdk:"bypass_static_routes"`
	DisableVPNRules        types.Bool  `tfsdk:"disable_vpn_rules"`
	DisableReplyTo         types.Bool  `tfsdk:"disable_reply_to"`
	DisableNegate          types.Bool  `tfsdk:"disable_negate"`
	NoAPIPA                types.Bool  `tfsdk:"no_apipa_block"`
	AliasesResolveInterval types.Int64 `tfsdk:"aliases_resolve_interval"`
	CheckAliasesURLCert    types.Bool  `tfsdk:"check_aliases_url_cert"`

	// Bogon Networks
	BogonsInterval types.String `tfsdk:"bogons_update_interval"`

	// NAT
	NATReflection             types.String `tfsdk:"nat_reflection_mode"`
	ReflectionTimeout         types.Int64  `tfsdk:"nat_reflection_timeout"`
	EnableBINATReflection     types.Bool   `tfsdk:"enable_binat_reflection"`
	EnableNATReflectionHelper types.Bool   `tfsdk:"enable_nat_reflection_helper"`
	TFTPInterface             types.String `tfsdk:"tftp_proxy_interfaces"`

	// State Timeouts
	TCPFirstTimeout       types.Int64 `tfsdk:"tcp_first_timeout"`
	TCPOpeningTimeout     types.Int64 `tfsdk:"tcp_opening_timeout"`
	TCPEstablishedTimeout types.Int64 `tfsdk:"tcp_established_timeout"`
	TCPClosingTimeout     types.Int64 `tfsdk:"tcp_closing_timeout"`
	TCPFinWaitTimeout     types.Int64 `tfsdk:"tcp_fin_wait_timeout"`
	TCPClosedTimeout      types.Int64 `tfsdk:"tcp_closed_timeout"`
	TCPTSDiffTimeout      types.Int64 `tfsdk:"tcp_tsdiff_timeout"`
	UDPFirstTimeout       types.Int64 `tfsdk:"udp_first_timeout"`
	UDPSingleTimeout      types.Int64 `tfsdk:"udp_single_timeout"`
	UDPMultipleTimeout    types.Int64 `tfsdk:"udp_multiple_timeout"`
	ICMPFirstTimeout      types.Int64 `tfsdk:"icmp_first_timeout"`
	ICMPErrorTimeout      types.Int64 `tfsdk:"icmp_error_timeout"`
	OtherFirstTimeout     types.Int64 `tfsdk:"other_first_timeout"`
	OtherSingleTimeout    types.Int64 `tfsdk:"other_single_timeout"`
	OtherMultipleTimeout  types.Int64 `tfsdk:"other_multiple_timeout"`
}

